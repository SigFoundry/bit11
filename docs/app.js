const localeButtons = Array.from(document.querySelectorAll("[data-locale]"));
const translatableNodes = Array.from(document.querySelectorAll("[data-i18n]"));
const titleNode = document.querySelector("title");
const descriptionNode = document.querySelector('meta[name="description"]');

function applyLocale(locale) {
  const resolved = copy[locale] ? locale : "en";
  document.documentElement.lang = resolved;
  localStorage.setItem("bit11-docs-locale", resolved);

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    const value = copy[resolved][key];
    if (typeof value === "string") {
      node.textContent = value;
    }
  });

  titleNode.textContent = copy[resolved].pageTitle;
  descriptionNode.setAttribute("content", copy[resolved].pageDescription);

  localeButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.locale === resolved);
  });
}

localeButtons.forEach((button) => {
  button.addEventListener("click", () => applyLocale(button.dataset.locale));
});

const preferredLocale = localStorage.getItem("bit11-docs-locale");
const browserLocale = navigator.language && navigator.language.toLowerCase().startsWith("ja") ? "ja" : "en";
applyLocale(preferredLocale || browserLocale);
